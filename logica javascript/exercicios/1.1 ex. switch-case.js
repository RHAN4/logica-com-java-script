let diaSemana = 2

switch (diaSemana) {
    case 1:
        console.log(`Dia selecionado: Domingo, final de semana.`)
    break

    case 2:
        console.log(`Dia selecionado: Segunda-feira, dia útil.`)
    break

    case 3:
        console.log(`Dia selecionado: Terça-feira, dia útil.`)
    break

    case 4:
        console.log(`Dia selecionado: Quarta-feira, dia útil.`)
    break

    case 5:
        console.log(`Dia selecionado: Quinta-feira, dia útil.`)
    break

    case 6:
        console.log(`Dia selecionado: Sexta-feira, dia útil.`)
    break

    case 7:
        console.log(`Dia selecionado: Sábado, final de semana.`)
    break

    default:
        console(`Dia inválido.`)
}
